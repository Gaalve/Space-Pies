package de.tu_berlin.Converter;

public class AtlasMeta {
    public final String name;
    public final String format;
    public final int w;
    public final int h;
    public final int scale;

    public AtlasMeta(String name, String format, int w, int h, int scale) {
        this.name = name;
        this.format = format;
        this.w = w;
        this.h = h;
        this.scale = scale;
    }


}
